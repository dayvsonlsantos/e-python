# Conversão de dados

# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# Tipos imutáveis e primitivos:
    # str, int, float, bool

# Para converter, basta passar dentro da respectiva classe:

print("To int", int("1"))
print("To int", type(int("1")))

print("To str", str(1))
print("To str", type(str(1)))

print("To float", float("1"))
print("To float", type(float("1")))

# Valor vazio será False | Valor com algo, será True
print("To bool", bool("1"))
print("To bool", type(bool("1")))